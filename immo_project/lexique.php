                <br/>
                Nous avons regroupé pour vous sur cette page tous les mots concernant notre métier de constructeur, promoteur et aménageur immobilier
				afin de vous permettre de naviguer en toute tranquilité sur notre site et ainsi de vous familiarisez aux termes liés au domaine
				pour peut être comprendre notre passion et découvrir l'univers de la construction.<br/><br/>
                <br/>
                <strong>CMI - CCMI:</strong> Le Constructeur de Maisons Individuelle est une personne physique ou morale bâtissant des logements individuels à 
				délais et prix convenus dans un CCMI – Contrat de Construction de Maison Individuelle signé par les deux parties cocontractantes : constructeur
				et maître d'ouvrage.<br/><br/>

				<strong>VEFA - Vente en l'Etat de Futur Achèvement:</strong> Contrat par lequel le vendeur transfère immédiatement à l’acquéreur ses droits sur 
				le sol ainsi que la propriété des constructions existantes. Les travaux à venir deviennent la propriété de l’acquéreur au fur et à mesure de 
				leur exécution. Le vendeur reste néanmoins le Maître d’ouvrage jusqu’à la fin des travaux.<br/><br/>

				<strong>Promoteur / Promotion:</strong> Le promoteur est une personne qui s’oblige envers le maître d’un ouvrage, à faire procéder, pour un 
				prix convenu, au moyen de contrats de louage d’ouvrage, à la réalisation d’un programme de construction et à tout ou une partie des démarches 
				juridiques, administratives et financières concernant la promotion.<br/><br/>

				<strong>CU (Certificat d’Urbanisme):</strong> C’est un acte administratif précisant la situation d’un terrain ou d’un bâtiment par rapport au 
				plan d’urbanisme de la ville. Il indique les règles d’urbanisme applicables en cas de construction. Il est recommandé de le demander en mairie 
				avant toute transaction immobilière.<br/><br/>

				<strong>DPE (Diagnostic de Performance Energétique):</strong> Il renseigne sur la performance énergétique d’un logement en évaluant sa 
				consommation d’énergie et son impact en termes d’émission de gaz à effet de serre.<br/><br/>

				<strong>Maître d’ouvrage:</strong> C’est la personne pour le compte de laquelle sont effectués les travaux de construction.<br/><br/>

				<strong>Maître d’œuvre:</strong> C’est la personne, morale ou physique, publique ou privée, chargée de traduire en termes techniques 
				les besoins du Maître d’ouvrage et de les faire réaliser (Conception des cahiers des charges, passation des marchés, rédaction des contrats,
				surveillance des travaux…).<br/><br/>

				<strong>Primo accédant:</strong> Personne qui n’a jamais été propriétaire d’une bien immobilier et qui achète pour la première fois un
				logement destiné à son usage personnel.<br/><br/>

				<strong>Permis de construire:</strong> Autorisation indispensable préalablement à la réalisation de toute nouvelle construction. 
				Un permis de construire et valable 2 ans à partir de sa notification par l’administration. Il doit être affiché sur un panneau de 
				taille règlementée, visible de la voie publique et comportant les indications sur la construction.<br/><br/>

				<strong>DROC (Déclaration Réglementaire d'Ouverture de Chantier):</strong> Lorsque le permis de construire est nécessaire, son bénéficiaire
				doit déclarer à la Mairie que les travaux ont effectivement commencé. Cette déclaration obligatoire objet d'un formulaire type, permet de 
				vérifier que l'opération de construction a bien été commencée avant l'expiration du permis de construire.<br/><br/>

				<strong>DAT (Déclaration d’Achèvement des Travaux):</strong> Lorsque les travaux, ayant fait l’objet d’un permis de construire sont terminés, 
				le maître d’œuvre dépose en mairie une D.A.T. au plus tard 30 jours après la fin des travaux. Cette démarche déclenche une "visite de 
				conformité", c'est-à-dire une vérification sur place de conformité des travaux effectués avec ceux autorisés par le permis de construire.<br/>
				<br/>

				<strong>Offre de prêts:</strong> Proposition écrite de crédit délivrée par l’établissement de crédit ou la banque afin de donner à 
				l’emprunteur une information complète sur les caractéristiques du crédit. L’offre de crédit immobilier soumise au code de la consommation 
				doit être adressée par voie postale. L’emprunteur ne peut accepter cette offre qu’après un délai de réflexion de 10 jours révolus après sa 
				réception, le cachet de la poste faisant foi.<br/><br/>

				<strong>Euribor (Les taux interbancaires):</strong> C’est l’un des deux principaux taux de référence du marché monétaire de la zone euro. 
				Les indices EURIBOR 3 mois et 12 mois sont respectivement les taux Interbancaires à 3 et 12 mois offerts en euros.<br/><br/>

				<strong>Remboursement anticipé:</strong> Remboursement par l’emprunteur d’une partie ou de la totalité de son prêt avant l’expiration de la 
				durée initiale de celui-ci. En principe, l’emprunteur doit payer une indemnité sauf dans certains cas.<br/><br/>

				<strong>RT2012 - Reglementation thermique 2012:</strong> C’est la nouvelle règlementation thermique française, née du Grenelle de 
				l’Environnement, et qui fait suite à la réglementation précédemment en vigueur, la RT2005. Elle vise à encadrer les émissions de gaz à effet 
				de serre dans le secteur du bâtiment et devra permettre de diviser par trois la consommation énergétique des bâtiments neufs dont les permis 
				de construire sont déposés à compter de la fin 2012. Cela concerne aussi bien les édifices tertiaires que résidentiels.<br/><br/>

				<strong>Tableau d’amortissement:</strong> Document qui détaille la répartition du montant de chaque échéance entre le capital, les intérêts 
				et les primes d’assurance le cas échéant. Il précise également le montant du capital restant dû après paiement de chaque échéance.<br/><br/>

				<strong>Promoteurs/constructeurs:</strong> Personnes qui réalisent des immeubles pour les vendre à des particuliers. Leurs logements sont 
				généralement commercialisés « en l’état de futur achèvement », c’est-à-dire en construction. Exerçant leur métier dans un cadre règlementaire 
				très strict, ils s’engagent à livrer la construction à une date et à un prix fixés à l’avance.<br/><br/>

				<strong>Maison environnementale:</strong> Maison dont la consommation en énergie primaire est inférieure ou égale à 50 kWh / m2 / an. 
				Les maisons dites basse consommation sont désormais conçues pour minimiser tous les besoins énergétiques : chauffage, eau chaude sanitaire, 
				éclairage, ventilation :

				- L’orientation du bâtiment afin de capter au mieux l’énergie solaire ;
				- L’étanchéité à l’air ;
				- L’ épaisseur d’isolant et des fenêtres;
 

				<strong>Franchise:</strong> La franchise est un contrat du droit commercial par lequel un commerçant dit " le franchiseur", concède à un autre
				commerçant dit " le franchisé ", le droit d'utiliser tout ou partie des droits incorporels qui lui appartiennent comme son nom commercial, ses
				procédés commerciaux, ses marques… généralement contre le versement d'un pourcentage sur son chiffre d'affaires ou d'un pourcentage calculé 
				sur ses bénéfices.<br/><br/>

				<strong>Franchisé:</strong> Le franchisé est une société qui est juridiquement indépendante et qui bénéficie, de par un contrat de franchise 
				passé avec un franchiseur, de droits d’utilisation de son enseigne, de ses marques et de ses procédés commerciaux en échange du versement d’un
				pourcentage sur son chiffre d’affaire ou ses bénéfices et éventuellement d’un droit d'entrée.<br/><br/>

				<strong>Lean manufacturing:</strong> C’est un système de gestion de production basé sur trois éléments fondamentaux. La réduction des coûts 
				par l’élimination des gaspillages, la production juste à temps et la qualité. Une fois que l’entreprise arrive à faire fonctionner ces trois 
				éléments de manière interdépendante, le système Lean donne des résultats durables et pérennes. L’entreprise industrielle devient compétitive 
				et adaptable à toute évolution du marché.<br/><br/>

				<strong>OS (Ordre de service):</strong> L’ordre de service est la décision du maître d’œuvre qui précise les modalités d’exécution de tout ou 
				partie des prestations qui constituent l’objet du marché. Il se caractérise par une réunion ou le maître d’œuvre validera avec son 
				interlocuteur Natilia,  les différents points nécessaires appelés « les conditions suspensives » (la position des cloisons et portes, la 
				plomberie, l’électricité, etc.).<br/><br/>

				<strong>MHE (Mise Hors D’Eau):</strong> Un bâtiment est hors d’eau lorsque les ouvrages de couverture et d’étanchéité sont achevés.<br/><br/>

				<strong>MHA (Mise Hors d’Air):</strong> Un bâtiment est hors d’air une fois les menuiseries extérieures pleines ou vitrées sont posées.<br/><br/>

				<strong>Viabilités/viabilisation:</strong> Fait de viabiliser, de réaliser des travaux d'aménagement afin de rendre un terrain habitable.
